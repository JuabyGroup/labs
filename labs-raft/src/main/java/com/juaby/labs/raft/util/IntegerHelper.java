public class IntegerHelper {

    public static byte[] fromIntToByteArray(int value) {
        return new byte[]{
                (byte) (value >>> 24),
                (byte) (value >>> 16),
                (byte) (value >>> 8),
                (byte) value};
    }

    public static int fromByteArrayToInt(byte[] b) {

        if ((b == null) || (b.length != 4)) {
            return 0;
        }

        return b[3] & 0xFF |
                (b[2] & 0xFF) << 8 |
                (b[1] & 0xFF) << 16 |
                (b[0] & 0xFF) << 24;
    }

}
